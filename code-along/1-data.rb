# In Ruby, there are different *types* of data:

# Numbers

puts 1
puts 2
puts 6+9
puts 1/4.0

# Non-numbers

puts "Hello!!"

puts "Hellowww" + "WOAH"

puts "tacos" *4

puts "tacos" + 3.to_s # lasdjf

# True, False

puts true

puts false

# Nothing

puts nil

# Variables

alpha = "tacos"

count = 3

puts alpha*count

# Combine strings and variables

puts "tacos: #{count}"

puts "#{alpha}: #{count}"

# String manipulation

puts "pspspss".length
puts "oh no".capitalize
puts "ohio".reverse